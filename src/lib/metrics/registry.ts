// ── Helpers ───────────────────────────────────────────────────────────────────

export function getMetricsByDomain(domain: Domain): MetricDefinition[] {
  return METRIC_REGISTRY.filter((m) => m.domain === domain);
}

export function getMetricById(id: string): MetricDefinition | undefined {
  return METRIC_REGISTRY.find((m) => m.id === id);
}

export function getManualMetrics(): MetricDefinition[] {
  return METRIC_REGISTRY.filter((m) => m.source === "manual");
}

export function getMetricsBySource(
  source: MetricDefinition["source"],
): MetricDefinition[] {
  return METRIC_REGISTRY.filter((m) => m.source === source);
}

export function getMetricsForDetector(detectorId: string): MetricDefinition[] {
  return METRIC_REGISTRY.filter((m) => m.detectorIds.includes(detectorId));
}
