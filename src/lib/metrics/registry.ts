import type { Domain } from "@/lib/analysis/domains";

export interface MetricDefinition {
  id: string;
  domain: Domain;
  name: string;
  shortName: string;
  description: string;
  unit: string;
  unitLabel: string;
  source: "wearable" | "chess_api" | "lichess_api" | "manual";
  inputType?: "slider" | "number" | "time" | "toggle" | "stepper" | "text";
  inputConfig?: {
    min?: number;
    max?: number;
    step?: number;
    placeholder?: string;
  };
  format: (value: number) => string;
  healthyRange?: { min: number; max: number; label: string };
  availableDevices?: string[];
  detectorIds: string[];
}

// ── Format helpers ────────────────────────────────────────────────────────────

const fmtHours   = (v: number) => `${v.toFixed(1)} hrs`;
const fmtPct     = (v: number) => `${Math.round(v)}%`;
const fmtMinutes = (v: number) => `${Math.round(v)} min`;
const fmtCount   = (v: number) => `${Math.round(v)}`;
const fmtBpm     = (v: number) => `${Math.round(v)} bpm`;
const fmtMs      = (v: number) => `${Math.round(v)} ms`;
const fmtElo     = (v: number) => `${Math.round(v)}`;
const fmtMg      = (v: number) => `${Math.round(v)} mg`;
const fmtLitres  = (v: number) => `${v.toFixed(1)} L`;
const fmtSlash10 = (v: number) => `${Math.round(v)}/10`;
const fmtKcal    = (v: number) => `${Math.round(v)} kcal`;
const fmtSteps   = (v: number) => `${Math.round(v).toLocaleString()}`;
const fmtPerGame = (v: number) => `${v.toFixed(1)}/game`;

function fmtTime(v: number): string {
  const h24 = Math.floor(v) % 24;
  const mins = Math.round((v % 1) * 60);
  const ampm = h24 >= 12 ? "PM" : "AM";
  const h12 = h24 % 12 || 12;
  return `${h12}:${String(mins).padStart(2, "0")} ${ampm}`;
}

